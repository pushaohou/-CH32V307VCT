/**
 * @file    oled.c
 * @brief   SSD1306 OLED 128×64 I2C 驱动 (I2C1 硬件I2C)
 * @note    中景园0.96寸OLED模块，驱动芯片SSD1306，I2C地址0x78(7bit=0x3C)
 */
#include "oled.h"
#include "debug.h"

/* 字库前向声明（定义在文件末尾） */
static const uint8_t font_6x8[][6];
static const uint8_t font_8x16[];

/* SSD1306 命令集 */
#define SSD1306_CMD_DISPLAY_OFF      0xAE
#define SSD1306_CMD_DISPLAY_ON       0xAF
#define SSD1306_CMD_SET_CONTRAST     0x81
#define SSD1306_CMD_ENTIRE_ON        0xA4
#define SSD1306_CMD_NORMAL_DISPLAY   0xA6
#define SSD1306_CMD_INVERT_DISPLAY   0xA7
#define SSD1306_CMD_SET_MUX_RATIO    0xA8
#define SSD1306_CMD_SET_OFFSET       0xD3
#define SSD1306_CMD_SET_START_LINE   0x40
#define SSD1306_CMD_SEG_REMAP        0xA0
#define SSD1306_CMD_COM_SCAN_DIR     0xC0
#define SSD1306_CMD_SET_COM_PINS     0xDA
#define SSD1306_CMD_SET_CLK_DIV      0xD5
#define SSD1306_CMD_SET_PRECHARGE    0xD9
#define SSD1306_CMD_SET_VCOMH        0xDB
#define SSD1306_CMD_CHARGE_PUMP      0x8D
#define SSD1306_CMD_MEM_ADDR_MODE    0x20

static const uint8_t OLED_INIT_CMDS[] = {
    0xAE,       /* Display OFF */
    0x00, 0x10, /* Set Column Address low/high */
    0x40,       /* Set Start Line */
    0x81, 0xCF, /* Set Contrast */
    0xA1,       /* Segment Remap (左右反:0xA0) */
    0xC8,       /* COM Scan Direction (上下反:0xC0) */
    0xA6,       /* Normal Display */
    0xA8, 0x3F, /* MUX Ratio = 63 (64 lines) */
    0xD3, 0x00, /* Display Offset = 0 */
    0xD5, 0x80, /* Clock Divide / Osc Freq */
    0xD9, 0xF1, /* Pre-charge Period */
    0xDA, 0x12, /* COM Pins Hardware Config */
    0xDB, 0x40, /* VCOMH Deselect Level */
    0x20, 0x02, /* Memory Addressing Mode = Page */
    0x8D, 0x14, /* Charge Pump ON */
    0xA4,       /* Entire Display ON (follow RAM) */
    0xA6,       /* Normal (non-inverted) */
    0xAF,       /* Display ON */
};

/* I2C 发送单字节（命令或数据） */
static void OLED_WriteByte(uint8_t dat, uint8_t cmd)
{
    while (I2C_GetFlagStatus(OLED_I2C, I2C_FLAG_BUSY))
        ;

    I2C_GenerateSTART(OLED_I2C, ENABLE);
    while (!I2C_CheckEvent(OLED_I2C, I2C_EVENT_MASTER_MODE_SELECT))
        ;

    I2C_Send7bitAddress(OLED_I2C, OLED_I2C_ADDR << 1, I2C_Direction_Transmitter);
    while (!I2C_CheckEvent(OLED_I2C, I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED))
        ;

    /* 控制字节: 0x00=命令, 0x40=数据 */
    I2C_SendData(OLED_I2C, cmd ? 0x40 : 0x00);
    while (!I2C_GetFlagStatus(OLED_I2C, I2C_FLAG_TXE))
        ;

    I2C_SendData(OLED_I2C, dat);
    while (!I2C_GetFlagStatus(OLED_I2C, I2C_FLAG_TXE))
        ;

    while (!I2C_CheckEvent(OLED_I2C, I2C_EVENT_MASTER_BYTE_TRANSMITTED))
        ;

    I2C_GenerateSTOP(OLED_I2C, ENABLE);
}

static void OLED_WriteCmd(uint8_t cmd)
{
    OLED_WriteByte(cmd, 0);
}

static void OLED_WriteData(uint8_t data)
{
    OLED_WriteByte(data, 1);
}

void OLED_Init(void)
{
    GPIO_InitTypeDef GPIO_InitStruct = {0};
    I2C_InitTypeDef  I2C_InitStruct  = {0};

    /* 时钟使能 */
    RCC_APB2PeriphClockCmd(OLED_I2C_SCL_CLK | OLED_I2C_SDA_CLK | OLED_RES_CLK, ENABLE);
    RCC_APB1PeriphClockCmd(OLED_I2C_CLK, ENABLE);

    /* RES引脚 — 推挽输出，先拉低复位再拉高 */
    GPIO_InitStruct.GPIO_Pin   = OLED_RES_PIN;
    GPIO_InitStruct.GPIO_Mode  = GPIO_Mode_Out_PP;
    GPIO_InitStruct.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(OLED_RES_PORT, &GPIO_InitStruct);

    GPIO_ResetBits(OLED_RES_PORT, OLED_RES_PIN);
    Delay_Ms(5);
    GPIO_SetBits(OLED_RES_PORT, OLED_RES_PIN);
    Delay_Ms(50);

    /* SCL / SDA — 复用开漏 */
    GPIO_InitStruct.GPIO_Pin   = OLED_I2C_SCL_PIN | OLED_I2C_SDA_PIN;
    GPIO_InitStruct.GPIO_Mode  = GPIO_Mode_AF_OD;
    GPIO_InitStruct.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(OLED_I2C_SCL_PORT, &GPIO_InitStruct);

    /* I2C 初始化 */
    I2C_InitStruct.I2C_ClockSpeed          = OLED_I2C_SPEED;
    I2C_InitStruct.I2C_Mode                = I2C_Mode_I2C;
    I2C_InitStruct.I2C_DutyCycle           = I2C_DutyCycle_16_9;
    I2C_InitStruct.I2C_OwnAddress1         = 0x00;
    I2C_InitStruct.I2C_Ack                 = I2C_Ack_Enable;
    I2C_InitStruct.I2C_AcknowledgedAddress = I2C_AcknowledgedAddress_7bit;
    I2C_Init(OLED_I2C, &I2C_InitStruct);
    I2C_Cmd(OLED_I2C, ENABLE);

    /* 发送初始化序列 */
    for (uint8_t i = 0; i < sizeof(OLED_INIT_CMDS); i++) {
        OLED_WriteCmd(OLED_INIT_CMDS[i]);
    }

    OLED_Clear();
}

void OLED_Clear(void)
{
    for (uint8_t page = 0; page < 8; page++) {
        OLED_WriteCmd(0xB0 + page);  /* 设置页地址 */
        OLED_WriteCmd(0x00);         /* 列低地址 */
        OLED_WriteCmd(0x10);         /* 列高地址 */
        for (uint8_t col = 0; col < 128; col++) {
            OLED_WriteData(0x00);
        }
    }
}

void OLED_Display_On(void)
{
    OLED_WriteCmd(SSD1306_CMD_CHARGE_PUMP);
    OLED_WriteCmd(0x14);
    OLED_WriteCmd(SSD1306_CMD_DISPLAY_ON);
}

void OLED_Display_Off(void)
{
    OLED_WriteCmd(SSD1306_CMD_CHARGE_PUMP);
    OLED_WriteCmd(0x10);
    OLED_WriteCmd(SSD1306_CMD_DISPLAY_OFF);
}

void OLED_ColorTurn(uint8_t mode)
{
    OLED_WriteCmd(mode ? SSD1306_CMD_INVERT_DISPLAY : SSD1306_CMD_NORMAL_DISPLAY);
}

void OLED_DisplayTurn(uint8_t mode)
{
    if (mode) {
        OLED_WriteCmd(0xC0);  /* COM 正常方向 */
        OLED_WriteCmd(0xA0);  /* SEG 正常方向 */
    } else {
        OLED_WriteCmd(0xC8);
        OLED_WriteCmd(0xA1);
    }
}

void OLED_Set_Pos(uint8_t x, uint8_t y)
{
    OLED_WriteCmd(0xB0 + y);
    OLED_WriteCmd(((x & 0xF0) >> 4) | 0x10);
    OLED_WriteCmd(x & 0x0F);
}

/**
 * @brief   显示 6×8 ASCII 字符
 * @param   x: 0~127 列
 * @param   y: 0~7   页(行)
 * @param   font_size: OLED_FONT_6x8 或 OLED_FONT_8x16
 */
void OLED_ShowChar(uint8_t x, uint8_t y, char chr, uint8_t font_size)
{
    uint8_t c = chr - ' ';

    if (c > 94) return;  /* 只支持可打印ASCII */

    if (font_size == OLED_FONT_6x8) {
        OLED_Set_Pos(x, y);
        for (uint8_t i = 0; i < 6; i++) {
            OLED_WriteData(font_6x8[c][i]);
        }
    } else if (font_size == OLED_FONT_8x16) {
        /* 上半部 */
        OLED_Set_Pos(x, y);
        for (uint8_t i = 0; i < 8; i++) {
            OLED_WriteData(font_8x16[c * 16 + i]);
        }
        /* 下半部 */
        OLED_Set_Pos(x, y + 1);
        for (uint8_t i = 0; i < 8; i++) {
            OLED_WriteData(font_8x16[c * 16 + i + 8]);
        }
    }
}

/**
 * @brief   显示字符串
 */
void OLED_ShowString(uint8_t x, uint8_t y, const char *str, uint8_t font_size)
{
    while (*str != '\0') {
        if (x > 127) break;
        OLED_ShowChar(x, y, *str, font_size);
        if (font_size == OLED_FONT_6x8) {
            x += 6;
        } else {
            x += 8;
        }
        str++;
    }
}

static uint32_t OLED_Pow(uint8_t m, uint8_t n)
{
    uint32_t result = 1;
    while (n--) result *= m;
    return result;
}

/**
 * @brief   显示无符号整数
 * @param   len: 显示位数（不足补空格）
 */
void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len, uint8_t font_size)
{
    uint8_t t;
    uint8_t enshow = 0;
    uint8_t dx = (font_size == OLED_FONT_6x8) ? 6 : 8;

    for (t = 0; t < len; t++) {
        uint32_t div = OLED_Pow(10, len - t - 1);
        uint8_t digit = (num / div) % 10;

        if (!enshow && t < (len - 1)) {
            if (digit == 0) {
                OLED_ShowChar(x + dx * t, y, ' ', font_size);
                continue;
            }
            enshow = 1;
        }
        OLED_ShowChar(x + dx * t, y, digit + '0', font_size);
    }
}

/**
 * @brief   显示浮点数
 * @param   int_len: 整数位数
 * @param   dec_len: 小数位数
 */
void OLED_ShowFloat(uint8_t x, uint8_t y, float num,
                    uint8_t int_len, uint8_t dec_len, uint8_t font_size)
{
    uint8_t dx = (font_size == OLED_FONT_6x8) ? 6 : 8;

    /* 负号处理 */
    if (num < 0) {
        OLED_ShowChar(x, y, '-', font_size);
        x += dx;
        num = -num;
    }

    /* 整数部分 */
    uint32_t int_part = (uint32_t)num;
    OLED_ShowNum(x, y, int_part, int_len, font_size);
    x += dx * int_len;

    /* 小数点 */
    OLED_ShowChar(x, y, '.', font_size);
    x += dx;

    /* 小数部分 */
    float frac = num - (float)int_part;
    for (uint8_t i = 0; i < dec_len; i++) {
        frac *= 10;
    }
    uint32_t dec_part = (uint32_t)(frac + 0.5f);
    OLED_ShowNum(x, y, dec_part, dec_len, font_size);
}

/* 中文字库前向声明 */
static const uint8_t F16x16[];
static const uint8_t F24x24[];

/**
 * @brief   显示中文（16×16 或 24×24 点阵）
 * @param   x: 0~127 列
 * @param   y: 0~7   页(行)
 * @param   index: 字库索引（0=第1个汉字）
 * @param   font_size: 16 或 24
 * @note    取模方式：共阴——列行式——逆向输出
 */
void OLED_ShowChinese(uint8_t x, uint8_t y, uint8_t index, uint8_t font_size)
{
    uint8_t wm;
    uint16_t adder;

    if (font_size == 16) {
        adder = 32 * index;
        OLED_Set_Pos(x, y);
        for (wm = 0; wm < 16; wm++) {
            OLED_WriteData(F16x16[adder]);
            adder += 1;
        }
        OLED_Set_Pos(x, y + 1);
        for (wm = 0; wm < 16; wm++) {
            OLED_WriteData(F16x16[adder]);
            adder += 1;
        }
    } else if (font_size == 24) {
        adder = 72 * index;
        OLED_Set_Pos(x, y);
        for (wm = 0; wm < 24; wm++) {
            OLED_WriteData(F24x24[adder]);
            adder += 1;
        }
        OLED_Set_Pos(x, y + 1);
        for (wm = 0; wm < 24; wm++) {
            OLED_WriteData(F24x24[adder]);
            adder += 1;
        }
        OLED_Set_Pos(x, y + 2);
        for (wm = 0; wm < 24; wm++) {
            OLED_WriteData(F24x24[adder]);
            adder += 1;
        }
    }
}

